def replacer(string):
    """
    Replaces delimiters in the input string to standardize the format.
    
    Args:
        string (str): The input string with sales data.
        
    Returns:
        str: The string with standardized delimiters.
    """
    return string.replace(",", "|").replace(";", "")

def splitter(string):
    """
    Splits the input string into a list using the pipe delimiter.
    
    Args:
        string (str): The input string with standardized delimiters.
        
    Returns:
        list: A list of strings split by the pipe delimiter.
    """
    return string.split("|")

def stripper(lst):
    """
    Strips whitespace and newline characters from each string in the list.
    
    Args:
        lst (list): A list of strings to be stripped.
        
    Returns:
        list: A list of stripped strings.
    """
    return [value.strip() for value in lst]

def sorter(lst):
    """
    Sorts the list into separate lists for customers, sales, thread sold, and date sold.
    
    Args:
        lst (list): A list of strings containing sales data.
        
    Returns:
        tuple: Four lists containing customers, sales, thread sold, and date sold.
    """
    customers, sales, thread_sold, date_sold = [], [], [], []
    for index in range(0, len(lst), 4):
        customers.append(lst[index])
        sales.append(lst[index + 1])
        thread_sold.append(lst[index + 2])
        date_sold.append(lst[index + 3])
    return customers, sales, thread_sold, date_sold

def sales_calculator(lst):
    """
    Converts a list of sales strings to floats and calculates the total sales.
    
    Args:
        lst (list): A list of sales strings.
        
    Returns:
        float: The total sales amount.
    """
    return sum(float(sale.strip("$")) for sale in lst)

def thread_count_sold(lst):
    """
    Counts the number of each thread color sold.
    
    Args:
        lst (list): A list of thread colors sold.
        
    Returns:
        dict: A dictionary with colors as keys and their counts as values.
    """
    color_counts = {}
    for item in lst:
        colors = item.split("&")
        for color in colors:
            color_counts[color] = color_counts.get(color, 0) + 1
    return color_counts

def color_count(color, color_counts):
    """
    Returns a formatted string with the count of a specific color sold.
    
    Args:
        color (str): The color to count.
        color_counts (dict): A dictionary with color counts.
        
    Returns:
        str: A formatted string with the count of the color.
    """
    count = color_counts.get(color, 0)
    return f"{count} of color {color} were sold today"

